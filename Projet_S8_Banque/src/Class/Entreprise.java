package Class;

/**
 * Class Entreprise with information of Entreprise
 * @author yja85
 *
 */
public class Entreprise {
	
	private String nom;
	private float ouverture;
	private float haut;
	private float bas;
	private int volume;
	private float veille;
	private float dernier;
	private float var;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public float getOuverture() {
		return ouverture;
	}
	public void setOuverture(float ouverture) {
		this.ouverture = ouverture;
	}
	public float getHaut() {
		return haut;
	}
	public void setHaut(float haut) {
		this.haut = haut;
	}
	public float getBas() {
		return bas;
	}
	public void setBas(float bas) {
		this.bas = bas;
	}
	public int getVolume() {
		return volume;
	}
	public void setVolume(int volume) {
		this.volume = volume;
	}
	public float getVeille() {
		return veille;
	}
	public void setVeille(float veille) {
		this.veille = veille;
	}
	public float getDernier() {
		return dernier;
	}
	public void setDernier(float dernier) {
		this.dernier = dernier;
	}
	public float getVar() {
		return var;
	}
	public void setVar(float var) {
		this.var = var;
	}

}
